"use client";

import Cal, { getCalApi } from "@calcom/embed-react";
import { useEffect } from "react";

function Meeting() {
  useEffect(() => {
    (async function () {
      const cal = await getCalApi({"namespace":"studio-sesh"});
      cal("ui", {"cssVarsPerTheme":{"light":{"cal-brand":"#0F172A"},"dark":{"cal-brand":"#0FA6E5"}},"hideEventTypeDetails":false,"layout":"month_view"});
    })();
  }, [])
  return <Cal namespace="studio-sesh"
    calLink="ben-chigozie/studio-sesh"
    style={{width:"100%",height:"100%",overflow:"scroll"}}
    config={{"layout":"month_view","useSlotsViewOnSmallScreen":"true"}}
  />;
};





export { Meeting };